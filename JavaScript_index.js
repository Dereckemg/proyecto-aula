$(document).ready(function(){
    
    $('#icon').click(function(){
        $('ul').toggleClass('show')
        $('h1').toggleClass('title-dont-show')    
    })

    $('#aboutUs').click(function(){
        $('ul').toggleClass('show')
        $('h1').toggleClass('title-show')

    })

    $('#contactanos').click(function(){
        $('ul').toggleClass('show')
        $('h1').toggleClass('title-show')

    })

    $('#redes').click(function(){
        $('ul').toggleClass('show')
        $('h1').toggleClass('title-show')

    })
  
})